def normalize_region(value: object) -> str:
    """Map aliases to the canonical 17-region names."""
    if value is None:
        raise ValueError("Region value is missing")
    key = str(value).strip()
    if key in REGION_ALIASES:
        return REGION_ALIASES[key]
    raise ValueError(f"Unknown Korean administrative region: {value!r}")
